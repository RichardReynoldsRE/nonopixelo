// Helper to get a scheduled puzzle by date
export function getScheduledPuzzle(dateString) {
  // First check for localStorage override
  const override = getDailyOverride(dateString)
  if (override) {
    return override
  }
  return scheduledDailyPuzzles[dateString] || null
}

// localStorage override functions for the editor
const DAILY_OVERRIDES_KEY = 'nonogram_daily_overrides'

export function getDailyOverride(dateString) {
  try {
    const overrides = JSON.parse(localStorage.getItem(DAILY_OVERRIDES_KEY) || '{}')
    return overrides[dateString] || null
  } catch {
    return null
  }
}

export function saveDailyOverride(dateString, puzzleData) {
  try {
    const overrides = JSON.parse(localStorage.getItem(DAILY_OVERRIDES_KEY) || '{}')
    overrides[dateString] = puzzleData
    localStorage.setItem(DAILY_OVERRIDES_KEY, JSON.stringify(overrides))
    return true
  } catch {
    return false
  }
}

export function deleteDailyOverride(dateString) {
  try {
    const overrides = JSON.parse(localStorage.getItem(DAILY_OVERRIDES_KEY) || '{}')
    delete overrides[dateString]
    localStorage.setItem(DAILY_OVERRIDES_KEY, JSON.stringify(overrides))
    return true
  } catch {
    return false
  }
}

export function getAllDailyOverrides() {
  try {
    return JSON.parse(localStorage.getItem(DAILY_OVERRIDES_KEY) || '{}')
  } catch {
    return {}
  }
}
